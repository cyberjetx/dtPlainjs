function BuildColumnSearch(dtable) {

    $(".dropdown-check-list").remove();
    dtable.columns().every(function () {

        let checklist = `<div id="list1" class="dropdown-check-list"><div><span class="anchor">Filter...</span><ul class="items" ></ul></div></div>`

        $(".casrep").hide();
        var column = this;
        var select = $(checklist)
            .appendTo($(column.header()))
            .on('change', function () {
                var val = ""; //$.fn.dataTable.util.escapeRegex($(this).val());
                let delim = "";

                $(this).find('input:checked').each(function (index, thisThisfinditemsli) {
                    //console.log($(thisThisfinditemsli).parent().text());
                    val += delim + '^' + $.fn.dataTable.util.escapeRegex($(thisThisfinditemsli).parent().text().trim()) + '$';
                    delim = "|";
                });
                let idx = dtable.colReorder.transpose(column.index(), 'toCurrent');
                dtable.column(idx)
                    //.search(val ? '^' + val + '$' : '', true, false)
                    .search(val, true, false)
                    .draw();
            })
            .on('click', function (e) {
                //Stop filter click from sorting 
                //console.log('th > select clicked!');
                e.stopPropagation();
            })
            .find('.anchor')
            .on('click', function () {
                $(this).parent().find(".items").toggle().focus();
                //console.log('click');
            })
            .parent();

        //add filter items 
        column.data().unique().sort().each(function (d, j) {
            //select.append('<option value="' + d + '">' + d + '</option>')
            select.find('.items').append('<li><input type="checkbox" />' + d + ' </li>')
            //console.log(d);
        });

    });
    // hide filter for select columns
    $(".dropdown-check-list").each(function (index, thisFilterSelect) {
        if ($(thisFilterSelect).parent().hasClass("notes")) {
            $(thisFilterSelect).remove();
        }
    });
}

